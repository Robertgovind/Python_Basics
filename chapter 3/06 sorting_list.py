numbers = [4, 2, 6, 7, 5, 2, 3, 9, 0]
fruits = ["Orange", "Mango", "Apple", "Banana", "jack fruit"]

# Ascending
numbers.sort()
fruits.sort()
print(numbers)
print(fruits)

# Descending
numbers.sort(reverse=True)
fruits.sort(reverse=True)
print(numbers)
print(fruits)


# Reversing a list
fruits.reverse()
print(fruits)
